package com.Basentjava;

//The Java break is used to break the loop. It breaks the current flow of the program at specified condition. 
//In case of inner loop, it breaks only inner loop.

public class J43_Break1 {

	public static void main(String[] args) {

     for(int i=1;i<=10;i++) {
    	 if(i==5) {
    		 break;
    	 }
    	 System.out.println(i);
    		
     }

	}

}
